#!/usr/bin/python3

def add(a, b):
    """Sum of numbers a and b

    Parameters
    ----------
    a -> int
        first number
    b -> int
        second number

    Returns
    -------
    a + b

    >>> print(add(2, 3))
    5
    >>> print(add(4, 5))
    9
    """
    return a + b

print(add(2, 3))
